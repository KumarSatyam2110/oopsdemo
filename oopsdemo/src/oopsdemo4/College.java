package oopsdemo4;

public class College {
	
	String name;
	String id;
	String University;
	String course;
	
	public College(String name, String id, String university, String course) {
		this.name = name;
		this.id = id;
		University = university;
		this.course = course;
	}

	public String getName() {
		return name;
	}

	public String getId() {
		return id;
	}

	public String getUniversity() {
		return University;
	}

	public String getCourse() {
		return course;
	}
	
	
	
	

}
